using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ForDongusuLoop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // 1'Den 10'a kadar olan sayıları alt alta yazdıran programı kodlayınız.
            // 1'Den 10'a kadar olan çift sayıları yazan kodu yapınız.

